#For Loop List
List=["apple",2.06,True,None,"Mango"]

for i in List:
    print(i)
print()

#OR
for i in range(len(List)):
    print(List[i])
print()

#While Loop List
i=0
while i < len(List):
    print(List[i])
    i+=1

